# taking and storing first float number in num1 variable
num1 = float(input("Enter first number: "))   
# taking and storing second float number in num2 variable
num2 = float(input("Enter second number: "))  
# adding the two numbers and storing it in sum variable
sum = num1 + num2                           
# printing the sum
print("Sum of two numbers is",format(sum,"<20.4f"))  